// 获取所有文本的扁平数组
export function getAllTexts(): string[] {
  return Object.values(textCategories).flatMap((cat) => cat.texts)
}

// 获取随机文本
export function getRandomText(): string {
  const all = getAllTexts()
  return all[Math.floor(Math.random() * all.length)]
}

// 按分类获取随机文本
export function getRandomTextByCategory(
  category: keyof typeof textCategories
): string {
  const texts = textCategories[category].texts
  return texts[Math.floor(Math.random() * texts.length)]
}

// 获取所有分类名称
export function getCategoryNames(): { key: string; name: string }[] {
  return Object.entries(textCategories).map(([key, val]) => ({
    key,
    name: val.name,
  }))
}
